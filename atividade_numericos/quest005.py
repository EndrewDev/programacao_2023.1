lagura = float(input('Lagira da parede:\n'))
altura = float(input('Altura da parede:\n'))
soma_parede = lagura * altura
tinta = soma_parede / 2
print(f'Sua parede te a dimensão de {lagura}x{altura}e sua área é de {soma_parede}m²')
print(f'Para pintar esse parede {soma_parede}m² vai precisa de {tinta}l de tintar')